def get_schema_info() -> dict[str, Any]:
    """
    MCP Tool — Returns the HR database schema as a formatted string.

    Used to inject schema context into LLM prompts so the model knows
    what tables and columns exist before generating SQL.

    Returns:
        dict with keys:
            - schema  (str)  : Human-readable schema description
            - success (bool) : Always True (static data)
    """
    schema = """
HR Database Schema (MySQL):

Table: employees
  - employee_id    INT          Primary key
  - full_name      VARCHAR      Full name (Arabic or English)
  - age            INT          Employee age
  - email          VARCHAR      Work email (SENSITIVE)
  - password       VARCHAR      Hashed password (SENSITIVE)
  - address        VARCHAR      Home address (SENSITIVE)
  - department_id  INT          FK → departments.department_id
  - position       VARCHAR      Job title
  - salary         DECIMAL      Monthly salary (SENSITIVE)
  - hiring_date    DATE         Date joined
  - marital_status ENUM         'Single' | 'Married' (SENSITIVE)
  - gender         ENUM         'Male' | 'Female'

Table: departments
  - department_id    INT     Primary key
  - department_name  VARCHAR Department name
  - location         VARCHAR Office location

Table: products
  - product_id       INT          Primary key
  - product_name     VARCHAR      Product name
  - manufacture_year YEAR         Year manufactured
  - color            VARCHAR      Product color
  - cost             DECIMAL      Cost price
  - price            DECIMAL      Selling price

Table: cities
  - city_id    INT     Primary key
  - city_name  VARCHAR City name

Table: sales
  - sale_id      INT          Primary key
  - product_id   INT          FK → products.product_id
  - city_id      INT          FK → cities.city_id
  - quantity     INT          Units sold
  - sale_date    DATE         Date of sale
  - total_amount DECIMAL      Total sale value
""".strip()

    return {"schema": schema, "success": True}


# ==============================================================
# Tool Registry — all tools in one dict for easy discovery
# ==============================================================

TOOL_REGISTRY: dict[str, callable] = {  
    "search_docs":       search_docs,
    "query_database":    query_database,
    "ask_public_llm":    ask_public_llm,
    "ask_private_llm":   ask_private_llm,
    "get_employee_info": get_employee_info,
    "get_schema_info":   get_schema_info,
}


def call_tool(name: str, **kwargs) -> dict[str, Any]:
    """
    Unified entry point to call any registered MCP tool by name.

    Args:
        name   (str): Tool name — must be a key in TOOL_REGISTRY.
        **kwargs    : Arguments forwarded to the tool function.

    Returns:
        dict: Tool result. Always contains at least {"success": bool}.

    Raises:
        ValueError: If tool name is not in the registry.

    Usage:
        result = call_tool("search_docs", query="leave policy", top_k=3)
        result = call_tool("ask_public_llm",
                           system_prompt="You are an HR assistant.",
                           user_message="How many vacation days do I get?")
    """
    if name not in TOOL_REGISTRY:
        available = list(TOOL_REGISTRY.keys())
        raise ValueError(f"Unknown tool '{name}'. Available tools: {available}")

    print(f"🔧 MCP tool called: {name}")
    return TOOL_REGISTRY[name](**kwargs)