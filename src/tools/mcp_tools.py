# Tool: get_employee_info
# ==============================================================

def get_employee_info(  
    name:       str | None = None,
    department: str | None = None,
    fields:     list[str] | None = None,
    limit:      int = 10,
) -> dict[str, Any]:
    """
    MCP Tool — Convenience wrapper for common employee lookups.

    Builds a safe SELECT query from parameters — no LLM needed for
    simple, predictable lookups (avoids LLM hallucinating column names).

    Args:
        name       (str | None)       : Employee name (partial match, LIKE).
        department (str | None)       : Department name (partial match, LIKE).
        fields     (list[str] | None) : Columns to return. Defaults to safe set.
        limit      (int)              : Max rows (default 10).

    Returns:
        Same shape as query_database() — rows, count, success, sql.

    Examples:
        get_employee_info(name="Ahmed")
        get_employee_info(department="IT", fields=["full_name", "position"])
        get_employee_info(department="HR", fields=["full_name", "email", "hiring_date"])
    """
    
    safe_default_fields = ["employee_id", "full_name", "email", "position", "department_id"]
    selected_fields     = fields if fields else safe_default_fields     

   
    allowed_columns = {
        "employee_id", "full_name", "age", "email",
        "address", "department_id", "position",
        "salary", "hiring_date", "marital_status", "gender"  
    }
    validated_fields = [f for f in selected_fields if f in allowed_columns]
    if not validated_fields:
        validated_fields = safe_default_fields

    columns = ", ".join(validated_fields)
    conditions: list[str] = []

    if name:
       
        safe_name = name.replace("'", "''")  # basic escape
        conditions.append(f"full_name LIKE '%{safe_name}%'")   # full_name matches your real column name

    if department:
        safe_dept = department.replace("'", "''")
        
        conditions.append(f"department_id IN (SELECT department_id FROM departments WHERE department_name LIKE '%{safe_dept}%')")

    where_clause = f"WHERE {' AND '.join(conditions)}" if conditions else ""
    sql = f"SELECT {columns} FROM employees {where_clause} LIMIT {limit}".strip()  # Removes extra spaces.

    return query_database(sql)


