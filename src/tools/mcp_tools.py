def search_docs(query: str, top_k: int = 4) -> dict[str, Any]:   
    """
    MCP Tool — Search company documents via Qdrant RAG.

    Args:
        query  (str): Natural language question (Arabic or English).
        top_k  (int): Number of document chunks to retrieve (default 4).

    Returns:
        dict with keys:
            - answer       (str)       : Synthesized answer from documents
            - source_texts (list[str]) : Raw text of retrieved chunks
            - scores       (list[float]): Similarity scores per chunk
            - success      (bool)      : False if an error occurred

    Usage (from nodes.py):
        result = search_docs("What is the remote work policy?")
        print(result["answer"])
    """
    try:
        engine   = get_query_engine(top_k=top_k)
        response = engine.query(query)

        
        try:
            nodes        = response.source_nodes   
            source_texts = [n.node.text for n in nodes]   
            scores       = [float(n.score) if n.score else 0.0 for n in nodes]
        except Exception:
            source_texts = []
            scores       = []

        return {
            "answer":       str(response),
            "source_texts": source_texts,
            "scores":       scores,
            "success":      True,
        }

    except Exception as e:
        return {
            "answer":       f"❌ Document search failed: {str(e)}",
            "source_texts": [],
            "scores":       [],
            "success":      False,
        }


