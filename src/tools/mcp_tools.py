# Tool: ask_public_llm
# ==============================================================

def ask_public_llm(
    system_prompt: str,
    user_message:  str,
    temperature:   float = 0.1
) -> dict[str, Any]:
    """
    MCP Tool — Send a prompt to GPT-4o (OpenAI cloud).

    Use for: routing decisions, SQL generation, general answers.
    Do NOT use for sensitive data — use ask_private_llm instead.

    Args:
        system_prompt (str)  : Instruction context for the model.
        user_message  (str)  : The actual user question or task.
        temperature   (float): 0.0–1.0, lower = more deterministic (default 0.1).

    Returns:
        dict with keys:
            - response (str)  : The model's text reply
            - model    (str)  : Model name used
            - success  (bool) : False if API call failed
    """
    try:
        resp = _openai_client.chat.completions.create(
            model=OPENAI_MODEL,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user",   "content": user_message},
            ],
            temperature=temperature,
        )
        return {
            "response": resp.choices[0].message.content.strip(),
            "model":    OPENAI_MODEL,
            "success":  True,
        }
    except Exception as e:
        return {
            "response": f"❌ GPT-4o call failed: {str(e)}",
            "model":    OPENAI_MODEL,
            "success":  False,
        }


