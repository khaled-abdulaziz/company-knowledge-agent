# ==============================================================

def query_database(sql: str) -> dict[str, Any]:
    """
    MCP Tool — Execute a read-only SQL query on MySQL.

    Safety: run_query() blocks non-SELECT statements before execution.

    Args:
        sql (str): A SELECT SQL query (generated by an LLM or hardcoded).

    Returns:
        dict with keys:
            - rows    (list[dict] | str): Rows as dicts, or error/warning string
            - count   (int)             : Number of rows returned (0 on error)
            - success (bool)            : False if blocked or errored
            - sql     (str)             : The query that was run (for logging)

    Usage:
        result = query_database("SELECT full_name, salary FROM employees LIMIT 5")
        for row in result["rows"]:
            print(row)
    """
    result = run_query(sql)

    if isinstance(result, str):  
        
        return {
            "rows":    result,
            "count":   0,
            "success": False,
            "sql":     sql,
        }

    return {
        "rows":    result,
        "count":   len(result),
        "success": True,
        "sql":     sql,
    }


