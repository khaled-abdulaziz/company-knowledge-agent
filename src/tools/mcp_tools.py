# Tool: ask_private_llm
# ==============================================================

def ask_private_llm(
    system_prompt: str,
    user_message:  str,
    temperature:   float = 0.1
) -> dict[str, Any]:
    """
    MCP Tool — Send a prompt to a local Ollama model.

    Use ONLY for sensitive queries (salary, national ID, customer data).
    Data never leaves the machine.

    Requires:
        - `ollama serve` running in the background
        - Model pulled: `ollama pull llama3`

    Args:
        system_prompt (str)  : Instruction context for the model.
        user_message  (str)  : The actual user question or task.
        temperature   (float): 0.0–1.0, lower = more deterministic (default 0.1).

    Returns:
        dict with keys:
            - response (str)  : The model's text reply
            - model    (str)  : "ollama:<model_name>"
            - success  (bool) : False if Ollama call failed
    """
    try:
        client = _OpenAI(
            base_url=f"{OLLAMA_BASE_URL}/v1",
            api_key="ollama",  
        )
        resp = client.chat.completions.create(
            model=OLLAMA_MODEL,
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user",   "content": user_message},
            ],
            temperature=temperature,
        )
        return {
            "response": resp.choices[0].message.content.strip(),
            "model":    f"ollama:{OLLAMA_MODEL}",
            "success":  True,
        }
    except Exception as e:
        return {
            "response": f"❌ Ollama call failed: {str(e)}",
            "model":    f"ollama:{OLLAMA_MODEL}",
            "success":  False,
        }


